<?php 
//logical operator
$Hasib = "400";
$farhana = "400";
if ($Hasib == $farhana){
    echo "Hasib has same number of money as farhana";
}
elseif ($Hasib > $farhana){
    echo "Hasib has more money than farhana";
}
else if ($Hasib < $farhana){
    echo "Hasib has less money than farhana";
}
else if ($Hasib >= $farhana){
    echo "Hasib has same or more money as farhana";
